public class operators {
  public static void main(String[] args) {
    double num1 = 10;
    double num2 = 9;

    // Addition
    System.out.println(num1 + num2);
    // sub
    System.out.println(num1 - num2);
    // mul
    System.out.println(num1 * num2);
    // div
    System.out.println(num1 / num2);
    // remainder s
    System.out.println(num1 % num2);

  }
}

